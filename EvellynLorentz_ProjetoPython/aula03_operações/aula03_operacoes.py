n1 = int(input(f'Digite o valor 1: '))
n2 = int(input(f'Digite o valor 2: '))
#soma
r = n1+n2
print(f'a soma é {r}')
#subtração
r = n1-n2
print(r)
#multiplicação
r = n1*n2
print(r)
#divisão
r = n1/n2
print(f'{r:.2f}')
#modulo
r = n1%n2
print(r)
#exponenciação
r = pow(n1,2)
print(f'{r:.2f}')
#raiz
r = pow(n1,1/2)
print(f'{r:.2f}')
